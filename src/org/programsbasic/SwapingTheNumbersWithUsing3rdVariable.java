package org.programsbasic;

public class SwapingTheNumbersWithUsing3rdVariable {

	public static void main(String[] args) {

		int a = 10;
		int b = 20;
		int c = 0;

		c = a;    // c=10
		a = b;    // a=20
		b = c;    // b=10

		System.out.println(a);
		System.out.println(b);
		//System.out.println(c);
	}

}

